print("딕셔너리 자료형 연습6")
# .get(key)
# 키에 대응되는 value를 돌려준다.
# 키값이 존재하지 않으면 None을 돌려준다.
# 인덱스를 모를때도 사용 가능
# dict_2["나이"]    ---- > 오류남. 딕셔너리에 나이가 없음.
# dict_2.get("나이")
print(dict_2.get("나이"))  #답 : None ---> 아예 비어있다는 의미가 아니라 데이터가 없다는 데이터가 있는것. 숫자 0과 비슷한 의미.
# 컴퓨터 메모리로 엄밀히 따질때 안에 쓰레기 값이 있을 수 있는데 이럴떄 데이터가 없다고 표현하자 라고 정한것.
print(dict_2.get("나이","나이 불명")) # 앞에 쓴 값이 데이터가 없을때 (None일 경우) 뒤의 값으로 출력됨.
print("\n")


