# 키값만 가져오기, 밸류 값만 가져오기
print("딕셔너리 자료형 연습5")
dict_2={1:'a', 2:'b', 3:'c'}
print("\n")

# key()
# 딕셔너리에서 key 값만 리스트 형태로 돌려준다.
dict_keys = dict_2.keys()
print(dict_keys)
print("\n")

# value()
# 딕셔너리에서 밸류 값만 리스트 형태로 돌려준다
dict_values = dict_2.values()
print(dict_values)
print("\n")

# item()
# 딕셔너리에서 key-value 쌍을 튜플로 묶은 값을 리스트 형태로 돌려준다
dict_items = dict_2.items()
print(dict_items)
print("\n")




